public class Rectangle
{
    //attributes
    private int x, y, height, width;

    public Rectangle(int x, int y)
    {
        this.x = x;
        this.y = y;
        this.height = 50;
        this.width = 50;
    }

    public int getX()
    {
        return this.x;
    }

    public int getY()
    {
        return this.y;
    }

    public int getHeight()
    {
        return this.height;
    }

    public int getWidth()
    {
        return this.width;
    }



}
